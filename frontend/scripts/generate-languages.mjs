// Generates src/lib/languages.generated.ts from the vendored OmniVoice languages.md.
//
// Usage:
//   node scripts/generate-languages.mjs
//
// To refresh the source snapshot first:
//   curl -sSL https://raw.githubusercontent.com/k2-fsa/OmniVoice/refs/heads/master/docs/languages.md \
//     -o scripts/languages.source.md
//
// The language list is NEVER hand-maintained — edit the source snapshot and re-run.

import { readFileSync, writeFileSync } from "node:fs"
import { fileURLToPath } from "node:url"
import { dirname, join } from "node:path"

/**
 * Parse the OmniVoice languages.md table into plain objects.
 * Columns: | # | Language | OmniVoice ID | ISO 639-3 | Duration (h) |
 * Header and separator rows are skipped (their first cell is not a number).
 * @param {string} markdown
 * @returns {{ name: string, id: string, isoCode: string, trainingHours: number }[]}
 */
export function parseLanguages(markdown) {
  const rows = []
  for (const line of markdown.split("\n")) {
    const trimmed = line.trim()
    if (!trimmed.startsWith("|")) continue
    const cells = trimmed
      .split("|")
      .slice(1, -1)
      .map((c) => c.trim())
    if (cells.length !== 5) continue
    const [num, name, id, isoCode, duration] = cells
    if (!/^\d+$/.test(num)) continue // skips header (#) and separator (--:) rows
    rows.push({ name, id, isoCode, trainingHours: parseFloat(duration) })
  }
  return rows
}

function renderModule(languages) {
  const banner =
    "// AUTO-GENERATED by scripts/generate-languages.mjs — DO NOT EDIT.\n" +
    "// Source: scripts/languages.source.md (OmniVoice docs/languages.md).\n\n"
  const type =
    "export interface RawLanguage {\n" +
    "  /** OmniVoice language ID — the value sent to the model. */\n" +
    "  id: string\n" +
    "  name: string\n" +
    "  /** ISO 639-3 code. */\n" +
    "  isoCode: string\n" +
    "  trainingHours: number\n" +
    "}\n\n"
  const body = languages
    .map(
      (l) =>
        `  { id: ${JSON.stringify(l.id)}, name: ${JSON.stringify(l.name)}, ` +
        `isoCode: ${JSON.stringify(l.isoCode)}, trainingHours: ${l.trainingHours} },`,
    )
    .join("\n")
  return `${banner}${type}export const GENERATED_LANGUAGES: RawLanguage[] = [\n${body}\n]\n`
}

function main() {
  const here = dirname(fileURLToPath(import.meta.url))
  const source = readFileSync(join(here, "languages.source.md"), "utf8")
  const languages = parseLanguages(source)
  const out = join(here, "..", "src", "lib", "languages.generated.ts")
  writeFileSync(out, renderModule(languages))
  console.log(`Generated ${languages.length} languages -> ${out}`)
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main()
}
